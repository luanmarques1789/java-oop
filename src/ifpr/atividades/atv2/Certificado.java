package ifpr.atividades.atv2;

public class Certificado {
	int livro;
	int folha;
	int pagina;
	int numeroDoCertificado;

	Certificado(int livro, int folha, int pagina, int numeroDoCertificado) {
		this.livro = livro;
		this.folha = folha;
		this.pagina = pagina;
		this.numeroDoCertificado = numeroDoCertificado;
	}
}
