package br.edu.ifpr.atividades.palestra.modelo;

public class Certificado {
	private int numLivro;
	private int numFolha;
	private int numPagina;
	private int numCertificado;

	public int getNumLivro() {
		return this.numLivro;
	}

	public void setNumLivro(int numLivro) {
		this.numLivro = numLivro;
	}

	public int getNumFolha() {
		return this.numFolha;
	}

	public void setNumFolha(int numFolha) {
		this.numFolha = numFolha;
	}

	public int getNumPagina() {
		return this.numPagina;
	}

	public void setNumPagina(int numPagina) {
		this.numPagina = numPagina;
	}

	public int getNumCertificado() {
		return this.numCertificado;
	}

	public void setNumCertificado(int numCertificado) {
		this.numCertificado = numCertificado;
	}

}
